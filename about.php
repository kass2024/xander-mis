// Function to get about page translation
function at($key) {
    global $about_translations, $current_lang;
    
    // Fallback to English if key missing
    if (isset($about_translations[$current_lang][$key])) {
        return $about_translations[$current_lang][$key];
    } elseif (isset($about_translations['en'][$key])) {
        return $about_translations['en'][$key];
    }
    
    return $key; // Return key itself as last resort
}

